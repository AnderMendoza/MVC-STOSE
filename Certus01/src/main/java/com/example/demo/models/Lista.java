package com.example.demo.models;

public class Lista {

	//Ayuda y contacto
	private String Cuenta;
	private String Pedidos;
	private String Envios;
	private String Devoluciones;
	private String Contactanos;
	
	
	public String getCuenta() {
		return Cuenta;
	}
	public void setCuenta(String cuenta) {
		Cuenta = cuenta;
	}
	public String getPedidos() {
		return Pedidos;
	}
	public void setPedidos(String pedidos) {
		Pedidos = pedidos;
	}
	public String getEnvios() {
		return Envios;
	}
	public void setEnvios(String envios) {
		Envios = envios;
	}
	public String getDevoluciones() {
		return Devoluciones;
	}
	public void setDevoluciones(String devoluciones) {
		Devoluciones = devoluciones;
	}
	public String getContactanos() {
		return Contactanos;
	}
	public void setContactanos(String contactanos) {
		Contactanos = contactanos;
	}
	
}
